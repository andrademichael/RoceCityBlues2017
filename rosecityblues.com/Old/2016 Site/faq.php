      <p class="header"><a name="pbj" id="pbj"></a>The Portland Blues & Jazz Dance Society (PB&J) </p>
 
      
  <p class="namesl40">What is PB&J?  What is your mission?</p>
 
  <p class="basepadded"><a href="http://pdxblues.com/about-us/" target="_new">PB&J is a Portland-based 501(c)(3)</a> non-profit dedicated to spreading the love of blues dance. Our mission statement is as follows: </p>
    <p class="basepadded">The Portland Blues and Jazz Dance Society (PB&J Dance Society) exists to create a safe, sustainable, and vibrant Blues and Jazz dance community through the free exchange of ideas, utilization of community knowledge, and promotion by example.  PB&J Dance Society seeks to reignite public interest in partnered dancing and grow Blues as a dance form through the exploration of various styles of movement and promotion of both solo and partnered social dancing.</p>
   
        
        <p class="namesl40">Why does being a non-profit matter?</p>
   
  <p class="basepadded"><em>Because community involvement and support matters.</em> It's important that events are driven by the common interests of the community, not just one or two people.</p>
        <p class="basepadded"><em>Because how your money is spent matters.</em> Every dollar of our event is being fed back directly into you – our community – and the thing you love to do – blues dance.</p>
        <p class="basepadded"><em>Because dedication to the blues community matters. </em> Our mission is to grow Blues as a dance form.  Our clear dedication - exemplified by our non-profit status - matters to the success of this event and to the strength of our community.</p>
         
       


