<p class="header"><a name="comp" id="comp"></a>Competitions Prizes and Other Dance Scholarships</p>
 
<p class="namesl40">Why are you holding competitions?</p>
 
<p class="basepadded">The Portland Blues & Jazz Dance Society is a sports non-profit, as such competition is a component of our core required activities. More than just a requirement, however, we feel friendly competition motivates the blues scene to progress its dance skill and gives participants a sense of achievement. Competition is also a means through which PB&J can provide scholarships as prizes.</p>
   
        
<p class="namesl40">Prizes are scholarships?</p>
   
<p class="basepadded">Yes! Competitions require competitors to display a level of mastery of blues dance and dance performance, implying a devotion to furthering their own abilities and the dance itself. While not a perfect means of distribution, we intend these prizes to act as scholarships to further competitors' education in blues and jazz dances.</p>
         
        
