      <p class="header"><a name="bd" id="bd"></a>Blues Dancing</p>
 
  <p class="namesl40">What is blues dancing?</p>
 
    <p class="basepadded">Blues initially evolved in casual dance communities, shaped from a desire to express the soul of the music from which we take the name of the dance (blues!). Due to the decentralized nature of the dance's origins, folks tend to have their own definition of what, exactly, the dance happens to be. </p>
    <p class="basepadded">In general, blues is a free-form style of social dancing that relies more on the connection with one’s partner than on having a set pattern of movements both (or all) participants know. Through this emphasis on a strong capacity to connect and communicate, dancers are given the freedom to turn their dance into a conversation, and from there, an unparalleled freedom of expression. Blues music itself contains an immense range of emotions, and blues dancing has the capacity to convey the same through partnered movement: from small and somber to big and celebratory, and everything in between.</p>
    <p class="basepadded">For more information, we recommend the following pages: <a href="http://en.wikipedia.org/wiki/Blues_dance" target="_new">Wikipedia entry</a>, <a href="http://www.blues-dance.com/aboutbluesdance/" target="_new">blues-dance.com</a>, <a href="http://www.2005.bluesdance.org.uk/32161/index.html" target="_new">For Blues Dancers</a>, <a href="http://www.bluesinheidelberg.com/whatis.html" target="_new">Blues in Heidelberg</a>. </p>
   
        
