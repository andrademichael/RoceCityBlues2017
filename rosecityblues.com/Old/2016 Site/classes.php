<p class="namesl40">Saturday</p>

<p class="basepadded"><em>11:00am - 12:15pm (Musicality, All levels)</em><br>
  <strong>Rhythms of Blues Dance</strong><br>
We'll use classic Blues vernacular to explore the various rhythms present in Blues music and dance.  By identifying our options we can expand our rhythmicality and fun.</p>

<p class="basepadded"><em>3:15pm - 4:30pm (Dips/Tricks, Int/Adv - BYO Partner Recommended)</em><br>
  <strong>Blues Dips and Tricks</strong><br>
We'll explore concepts to begin to create more dynamic physical play in partnering, with lots of ideas and demonstrations of cool dips and tricks, as well as the technique that allows them to happen organically.</p>

<p class="basepadded"><em>4:45pm - 6pm (Group Dances, All Levels)</em><br>
  <strong>Bringing the Party</strong><br>
Blues Dance has a rich history as an American Folk dance.  Come learn some of the party dances that exist in our culture to Bring the Party from a competitive singles scene to a thriving community event.</p>

<br>

<p class="namesl40">Sunday</p>

<p class="basepadded"><em>12:00pm - 1:15pm (Mechanics/Vocabulary, Int/Adv)</em><br>
  <strong>Partnering Magic</strong><br>
This class will cover the secrets to magical lead/follow connection and maneuvering, as well as teach you a few badass moves.</p>

<p class="basepadded"><em>4:15pm - 5:30pm (Conceptual/Practical, All Levels)</em><br>
  <strong>Choreographer's Workshop </strong><br>
Hopefully we've inspired you this weekend.  A step by step how to create your own choreography.  Choreography is a great way to develop aspects of your dancing that translate onto the social floor, expand movement possibilities, develop your ability to transition well, express your creative side, show dance possibilities that aren't appropriate for the social floor.  Come back next year and show us what you've been up to!</p>

