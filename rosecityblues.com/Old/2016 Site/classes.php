<p class="header">Mike and Dan Legenthal</p>

<p class="namesl40">Friday</p>

<p class="basepadded"><em>3:00pm - 4:15pm (Rhythm, All levels)</em><br>
  <strong>Cool Rhythms for Hot Dancing</strong><br>
You know that blues is rhythmically driven.  So why is so much of your dancing just a series of 2-beat steps?  Let's show some love to other beats- and some sweet syncopations!  </p>

<p class="basepadded"><em>4:30pm - 5:45pm (Musicality, Int/Advanced)</em><br>
  <strong>"Dynamicality" </strong><br>
Blues musicians are masters of repetition.  But they also make clever choices that lead to a wider range of changes and dynamics than you might not notice at a casual listen.  We're going to explore choices- both musical and movement-based in nature- that can push the dynamic range of your dancing, and make you more musical in the process!</p>

<br>

<p class="namesl40">Saturday</p>

<p class="basepadded"><em>11:00am to 12:15pm (Close Embrace, Int/Adv)</em><br>
  <strong>Four-Legged Grilled Cheese </strong><br>
Close embrace can be tricky, but it can also be absolutely magical. We want to give you the ability to move as though you are one with your partner. We’re going to celebrate the arts of leading and following, and try to reframe the way we use our bodies to get that gorgeous close embrace connection.</p>

<p class="basepadded"><em>12:30pm - 1:45pm (Dancing to the Band, All Levels)</em><br>
  <strong>The Twilight Zone</strong><br>
Blues music influenced, and was influenced by, many other styles of music.  Blues musicians today continue with that tradition, and artists like Maria Muldaur are comfortable playing everything from jazzy, small-combo pieces, to gritty electric guitar-driven juke-joint tunes, and everything in between.  We especially love songs that live in that in between space.  Welcome... to the twilight zone.</p>

<br>

<p class="namesl40">Sunday</p>

<p class="basepadded"><em>12:00pm - 1:15pm (History/Musicality, All levels)</em><br>
  <strong>Historical Elements of Blues</strong><br>
Really digging on musicality and dancing authentically means taking time to listen to music intentionally. Blues music has a variety of influences, from the stages of vaudeville to the moans of the Delta. Part history class, part musicality class, this class will address historical components of Blues music history that should shape our dancing.</p>

<p class="basepadded"><em>1:30pm - 2:45pm (Social Cues/Embraces, All Levels)</em><br>
  <strong>Psychic Mindpowers</strong><br>
Once upon a time, Mike realized that when she wanted to have cuddly dances, all the leads magically slipped into cuddle mode! Intrigued, she investigated, and found that it wasn’t magic- it was mind powers… and of course, body language. Leaders and followers alike, come learn how to lay down (and pick up) subtle cues so that you can get your partnership on the same page.</p>

<br>
<br>

<p class="header">Rachel Stirling and Andrew Smith</p>

<p class="namesl40">Friday</p></p>

<p class="basepadded"><em>4:30pm - 5:45pm (Conceptual/Partnering, Int/Adv)</em><br>
  <strong>Conversational Dancing</strong><br>
Blues allows for a lot of self-expression, so how do we both express in a conversation without derailing each other? Here we will discover how to create a dialogue within the lead-follow structure that allows both partners to contribute equally. We will explore tricksy questions such as, “Can a follow create movement without becoming the lead?” Come join us for some dynamic back-and-forth with your partner to inspire movement and play. </p>

<p class="basepadded"><em>6:00pm - 7:15pm (Conditioning/Technique, All Levels)</em><br>
  <strong>Hang in the Balance</strong><br>
Finding our balance is critical for all forms of dance, and Blues is no exception. Here we will learn how to improve our balance, discover its impact in solo and partnered movement, and experiment with it in fun ways to create new movement.</p>

<br>

<p class="namesl40">Saturday</p>

<p class="basepadded"><em>12:30pm - 1:45pm (Connection/Momentum, Int/Adv - BYO Partner Recommended)</em><br>
  <strong>More-mentum for More Winning</strong><br>
You understand the basics of momentum, now what? 1. Take our class on using momentum with uncommon connection points to create new shapes and dynamics. 2. … 3. PROFIT! This class is for advanced dancers or Bring Your Own Partner.</p>

<p class="namesl40">Saturday</p>
 
<p class="basepadded"><em>3:15-4:30 (Inspiration, All Levels)</em><br>
  <strong>Lose Yourself to Dance</strong><br>
Get up and throw down: here we look at how to unleash your creative spirit and leave it all on the dance floor - first solo, then with a partner. Check your ego at the door, open your heart, and share with the world how you Dance; it’s time to bring it.</p>

<br>

<p class="namesl40">Sunday</p>

<p class="basepadded"><em>1:30pm - 2:45pm (Conceptual/Creative, All Levels)</em><br>
  <strong>Tell Me A Story</strong><br>
A fundamental part of the human experience, we look at how to create story through movement, both solo and partnered. Be prepared to push your comfort level in expression, to get silly and serious and try things out without knowing where they will go. We can’t promise you’ll be the next J.K. Rowling of the dance floor, but you’ll certainly have a lot of fun out there.</p>

<p class="basepadded"><em>4:15pm - 5:30pm (Dancing to the Band, All Levels)</em><br>
  <strong>Groove Me</strong><br>
Get ready to groove that body with a class of soulful and funky blues. Never get lost again when the beat gets janky and learn how to wow your partner when a smooth slow jam comes on.  We’ll show you how to find a natural body movement that fits perfectly to the music of tonight’s performer LaRhonda Steele.</p>

