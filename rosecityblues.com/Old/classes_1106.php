<p class="base"><strong>Judging<br>Friday, 6:00 - 7:30 (All Levels)</strong><br>
<em>Brenda Russell</em><br>
This word has developed a negative connotation in our society.  We will look at, explore, and practice the skill of evaluating movement.  You will learn to observe one's alignments and movements, notice elements and characteristics, articulate your perspective.  This can be done from a neutral and matter of fact position, creating a neutral mirror, allowing other participants to make choices about what they want to portray.  Group participation in feedback based on observation of one another for the purpose of personal awareness and discretion will be practiced in this class.</p>

