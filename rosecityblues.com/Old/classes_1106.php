<p class="base"><strong>Lag<br>Sunday, 10:00 - 12:30 (Intermediate)</strong><br>
<em>Julie Brown & Shawn Hershey</em><br>
Lag is both a rhythmic technique and a partnered technique essential to blues dancing; it creates that delicious slowness and grit, and can be used for subtle rhythmic play. In this class, we’ll explore rhythmic as well as partnered lag, focusing primarily on lag as a follower contribution. Followers will learn different techniques for creating lag and how to use it to create cool rhythmic and dynamic feels. Leaders will learn different techniques for responding to follower lag and how to use followers’ lag choices as inspiration for shaping the dance.</p>

