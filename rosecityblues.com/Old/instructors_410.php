<p class="base">She studied under Norma Claire, whom Catherine calls her mentor, and whose pedagogy enabled her to explore every part of her body in order to combine physical sensations and observations.  With this powerful tool, Catherine continued her training in African dance with Elsa Wolliaston, Hawa Kouyaté, Koffi Koko, Moustapha Bangouroura, Mouminatou Camara, and traveled to Burkina Faso, Senegal, and Guinée to polish her art.  Catherine also spent five years at a professional dance school in Paris where she studied Modern, Jazz, contemporary, and anatomy. </p>
